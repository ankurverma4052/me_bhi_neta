		<section id="servicepara">
			<h3 id="servicehead3">1- Amrut Schemes:-</h3>
				<p>Amrut Schemes launched by Ministry of Urban Development on June 25, 2015, to enable better living and drive economic growth stressing on the need for people-centric urban planning and development.</p>

			<h3 id="servicehead3">2- Antyodaya Anna Yojana:-</h3>
				<p>Antyodaya Anna Yojana launched by NDA government on 25 December 2000 Under the plan 1 crore of the poorest among the (Below Poverty Line)BPL families secured under the focused on open dissemination framework are recognized. The issue of Ration Cards Following the acknowledgment of Antyodaya families, one of a kind share cards to be perceived an "Antyodaya Ration Card" must be given to the Antyodaya families by the picked control. The plan has been additionally extended twice by extra 50 lakh BPL families each in June 2003 and in August 2004, thus covering 2 crore families under the AAY conspire.</p>

			<h3 id="servicehead3">3- Atal Pension Yojana:-</h3>
				<p>Atal Pension Yojana launched on May 9, 2015, for Pension under Social Sector Scheme pertaining to Pension Sector.</p>

			<h3 id="servicehead3">4- Bachat Lamp Yojna:-</h3>
				<p>Bachat Lamp Yojna launched by the ministry of power in 2009 for Electrification to reduce the cost of compact fluorescent lamps.</p>

			<h3 id="servicehead3">5- Central Government Health Scheme:-</h3>
				<p>Central Government Health Scheme launched by the ministry of health and family welfare in 1954 for Health for comprehensive medical care facilities to Central Government employees and their family members.</p>

			<h3 id="servicehead3">6- Deendayal Disabled Rehabilitation Scheme:-</h3>
				<p>Deendayal Disabled Rehabilitation Scheme launched by the ministry of social justice and empowerment in 2003   for Social Justice to Create an enabling environment to ensure equal opportunities, equity, social justice and empowerment of persons with disabilities.</p>

			<h3 id="servicehead3">7- Deen Dayal Upadhyaya Grameen Kaushalya Yojana:-</h3>
				<p>Deen Dayal Upadhyaya Grameen Kaushalya Yojana launched by the ministry of rural development in 2015    for Rural Development and It is a Government of India Project to engage rural youth especially BPL and SC/ST segment of the population, in gainful employment through skill training programmes.</p>

			<h3 id="servicehead3">8- Digital India Programme:-</h3>
				<p>Digital India Programme launched by the ministry of commerce and information technology on July 1, 2015, with a budget of 1 Lakh Crore for Digitally Empowering Nation which Aims to ensure that government services are available to citizens electronically and people get benefited from the latest information and communication technology.</p>

			<h3 id="servicehead3">9- Gramin Bhandaran Yojana:-</h3>
				<p>Gramin Bhandaran Yojana launched by the ministry of agriculture and farmers welfare on March 31, 2007, for Creation of scientific storage capacity with allied facilities in rural areas to meet the requirements of farmers for storing farm produce, processed farm produce and agricultural inputs. Improve their marketability through the promotion of grading, standardization and quality control of agricultural produce.</p>

			<h3 id="servicehead3">10- Pradhan Mantri Gramin Awaas Yojana:- </h3>
			 	<p>Pradhan Mantri Gramin Awaas Yojana launched by the ministry of rural development in 1985 to Provide financial assistance to rural poor for constructing their houses themselves.</p>

			<h3 id="servicehead3">11- Pradhan Mantri Matritva Vandana Yojana:- </h3>
				<p>Pradhan Mantri Matritva Vandana Yojana launched by the ministry of women &amp; child development in 2010 for Mother Care which gives a cash incentive of Rs. 4000 to women (19 years and above) for the first two live births.</p>

			<h3 id="servicehead3">12- Integrated Child Development Services:- </h3>
				<p>Integrated Child Development Services launched by the ministry of child and women development on October 2, 1975, for Child Development to tackle malnutrition and health problems in children below 6 years of age and their mothers.</p>

			<h3 id="servicehead3">13- Integrated Rural Development Programme:- </h3>
				<p>Integrated Rural Development Programme launched by the ministry of rural development in 1978 for Rural Development which gives self-employment program to raise the income-generation capacity of target groups among the poor and The scheme has been merged with another scheme named Swarnajayanti Gram Swarozgar Yojana (SGSY) since 01.04 1999.</p>

			<h3 id="servicehead3">14- Janani Suraksha Yojana:- </h3>
				<p>Janani Suraksha Yojana launched by the ministry of health and family welfare in 2005 for Mother Care which gives One-time cash incentive to pregnant women for institutional/home births through skilled assistance.</p>

			<h3 id="servicehead3">15- Jawaharlal Nehru National Urban Renewal Mission:- </h3>
				<p>Jawaharlal Nehru National Urban Renewal Mission launched by the ministry of urban development on December 3, 2005, for Urban Development which is a programme meant to improve the quality of life and infrastructure in the cities. To be replaced by Atal Mission for Rejuvenation and Urban Transformation.</p>

			<h3 id="servicehead3">16- Kasturba Gandhi Balika Vidyalaya</h3>
				<p>Kasturba Gandhi Balika Vidyalaya launched by the ministry of human resource development in July 2004 for Educational facilities (residential schools) for girls belonging to SC, ST, OBC, minority communities and families below the poverty line(BPL) in Educationally Backward Blocks.</p>

			<h3 id="servicehead3">17- INSPIRE Programme:- </h3>
				<p>INSPIRE Programme launched by Department of Science and Technology (India) for Scholarships for top Science students, Fellowships for pursuing Ph.D., Research Grants to researchers.</p>

			<h3 id="servicehead3">18- Kishore Vaigyanik Protsahan Yojana </h3>
				<p>Kishore Vaigyanik Protsahan Yojana launched by the ministry of science and technology in 1999 which is a Scholarship program to encourage students to take up research careers in the areas of basic sciences, engineering, and medicine.</p>

			<h3 id="servicehead3">19- Livestock Insurance:- </h3>
				<p> Livestock Insurance launched by the ministry of agriculture and farmers welfare to provide Insurance to cattle and attaining qualitative improvement in livestock and their products.</p>

			<h3 id="servicehead3">20- Mahatma Gandhi National Rural Employment Gurantee Act:- </h3>
				<p>Mahatma Gandhi National Rural Employment Gurantee Act launched by the ministry of rural development on February 6, 2006, with a budget of Rs. 40,000 crore in 2010–11 for Rural Wage Employment to give Legal guarantee for one hundred days of employment in every financial year to adult members of any rural household willing to do public work-related unskilled manual work at the statutory minimum wage of Rs. 120 per day at 2009 prices.</p>

			<h3 id="servicehead3">21- Members of Parliament Local Area Development Scheme:- </h3>
				<p>Members of Parliament Local Area Development Scheme launched by the ministry of statistics and programme implementation on December 23, 1993, in which each MP has the choice to suggest to the District Collector for, works to the tune of Rs.5 Crores per annum to be taken up in his/her constituency. The Rajya Sabha Member of Parliament can recommend works in one or more districts in the State from where he/she has been elected.</p>

			<h3 id="servicehead3">22- Midday Meal Scheme:- </h3>
				<p>Midday Meal Scheme launched by the ministry of human resource development on August 15, 1995, for Health and Education which gives Lunch (free of cost) to school-children on all working days.</p>

			<h3 id="servicehead3">23- Namami Gange Programme:- </h3>
				<p>Namami Gange Programme launched by the ministry of water resources, river development &amp; Ganga rejuvenation on March 1995 with a budget of Rs.20000 crore for 5 years Clean &amp; Protect Ganga which Integrates the efforts to clean and protect the River Ganga in a comprehensive manner.</p>

			<h3 id="servicehead3">24-National Literacy Mission Programme:- </h3>
				<p>National Literacy Mission Programme launched by the ministry of human resource development on May 5, 1988, for Education which Makes 80 million adults in the age group of 15 – 35 literate.</p>


			<h3 id="servicehead3">25- National Pension System:- </h3>
				<p> National Pension System SCHEME launched on January 1, 2004 for Contribution based pension system.</p>

			<h3 id="servicehead3">26- National Scheme of Welfare of Fishermen:- </h3>
				<p>National Scheme of Welfare of Fishermen launched by Ministry of Agriculture and farmers welfare for Financial assistance to fishers for construction of the house, community hall for recreation and common working place and installation of tube-wells for drinking water.</p>

			<h3 id="servicehead3">27- National Service Scheme:- </h3>
				<p>National Service Scheme launched by the ministry of youth affairs and sports in 1969 for Personality development through social (or community) service.</p>

			<h3 id="servicehead3">28- National Social Assistance Scheme:- </h3>
				<p>National Social Assistance Scheme launched by the ministry of rural development on August 15, 1995, for Pension and Public assistance to its citizens in case of unemployment, old age, sickness and disablement and in other cases of undeserved want.</p>

			<h3 id="servicehead3">29- Pradhan Mantri Adarsh Gram Yojana:- </h3>
				<p>Pradhan Mantri Adarsh Gram Yojana launched by the ministry of rural development on July 23, 2010, under Model Village programme for the Integrated development of Schedule Caste majority villages in four states. </p>

			<h3 id="servicehead3">30- Pradhan Mantri Kaushal Vikas Yojana:- </h3>
				<p>Pradhan Mantri Kaushal Vikas Yojana launched by the ministry of skill development and entrepreneurship in April 2015 under skill development initiative schemes to provide encouragement to youth for development of employable skills by providing monetary rewards by recognition of prior learning or by undergoing training at affiliated centers.</p>

			<h3 id="servicehead3">31- Pradhan Mantri Suraksha Bima Yojana:- </h3>
				<p>Pradhan Mantri Suraksha Bima Yojana launched by the ministry of finance on May 9, 2015, to provide accidental Insurance with a premium of Rs. 12 per year.</p>

			<h3 id="servicehead3">32- Pradhan Mantri Jeevan Jyoti Bima Yojana:- </h3>
				<p>Pradhan Mantri Jeevan Jyoti Bima Yojana launched by the ministry of finance on May 9, 2015, to give Life insurance of Rs. 2 lakh with a premium of Rs. 330 per year.</p>

			<h3 id="servicehead3">33- Pradhan Mantri Jan Dhan Yojana</h3>
				<p>Pradhan Mantri Jan Dhan Yojana launched by the ministry of finance on August 28, 2014, for Financial Inclusion programme under National Mission for Financial Inclusion to ensure access to financial services, namely Banking Savings &amp; Deposit Accounts, Remittance, Credit, Insurance, Pension in an affordable manner.</p>

			<h3 id="servicehead3">34- Pradhan Mantri Gram Sadak Yojana:- </h3>
				<p>Pradhan Mantri Gram Sadak Yojana launched by the ministry of rural development on December 25, 2000, for Rural Development by constructing Good all-weather road connectivity to unconnected villages.</p>

			<h3 id="servicehead3">35- Rajiv Awas Yojana:- </h3>
				<p>Rajiv Awas Yojana launched by the housing and urban poverty alleviation in 2013 for Urban Housing. It envisages a "Slum Free India" with inclusive and equitable cities in which every citizen has access to basic civic infrastructure and social amenities and decent shelter.</p>

			<h3 id="servicehead3">36- Rajiv Gandhi Grameen Vidyutikaran Yojana</h3>
				<p>Rajiv Gandhi Grameen Vidyutikaran Yojana launched by the ministry of power in April 2005 which is to be replaced by Deen Dayal Upadhyaya Gram Jyoti Yojana for Rural Electrification Programme for the creation of Rural Electricity Infrastructure &amp; Household Electrification for providing access to electricity to rural households.</p>

			<h3 id="servicehead3">37- Rashtriya Krishi Vikas Yojana</h3>
				<p>Rashtriya Krishi Vikas Yojana launched by the ministry of agriculture and farmers welfare on August 1, 2007, for Agriculture to achieve 4% annual growth in agriculture through development of Agriculture and its allied sectors during the XI Plan period.</p>

			<h3 id="servicehead3">38- Rashtriya Swasthya Bima Yojana </h3>
				<p>Rashtriya Swasthya Bima Yojana launched by the ministry of health and family welfare on April 1, 2008, to give health insurance to the poor (BPL), Domestic workers, MGNERGA workers, Rickshaw pullers, Building and other construction workers, and many other categories as may be identified by the respective states.</p>

			<h3 id="servicehead3">39- Revised National Tuberculosis Control Program:- </h3>
				<p>Revised National Tuberculosis Control Program launched by the ministry of health and family welfare in1997    under Health Tuberculosis control initiative.</p>

			<h3 id="servicehead3">40- Rajiv Gandhi Scheme for Empowerment of Adolescent Boys(Saksham):- </h3>
				<p>Rajiv Gandhi Scheme for Empowerment of Adolescent Boys launched by the ministry of women and child development in 2014 for Skill Development which Aims at all-round development of Adolescent Boys and makes them self-reliant, gender-sensitive and aware citizens, when they grow up. It covers all adolescent boys (both schools going and out of school) in the age-group of 11 to 18 years subdivided into two categories, viz. 11-14 &amp; 14–18 years. In 2014–15, an allocation of Rs. 25 crores is made for the scheme.</p>

			<h3 id="servicehead3">41- Rajiv Gandhi Scheme for Empowerment of Adolescent Girls(Sabla):- </h3>
				<p>Rajiv Gandhi Scheme for Empowerment of Adolescent Girls launched by the ministry of women and child development in 2011 for Skill Development and Empowering adolescent girls (Age) of 11–18 years with the focus on out-of-school girls by improvement in their nutritional and health status and upgrading various skills like home skills, life skills, and vocational skills. Merged Nutrition Programme for Adolescent Girls (NPAG) and Kishori Shakti Yojana (KSY).</p>

			<h3 id="servicehead3">42- Sampoorna Grameen Rozgar Yojana:- </h3>
				<p>Sampoorna Grameen Rozgar Yojana launched by the ministry of rural development on September 25, 2001, for Rural Self Employment and Providing additional wage employment and food security, alongside the creation of durable community assets in rural areas.</p>

			<h3 id="servicehead3">43- Swabhimaan:- </h3>
				<p>Swabhimaan launched by the ministry of finance on February 15, 2011, for Financial Inclusion to make banking facility available to all citizens and to get 5 crore accounts opened by Mar 2012. Replaced by Pradhan Mantri Jan Dhan Yojana.</p>


			<h3 id="servicehead3">44- Swarnajayanti Gram Swarozgar Yojana:- </h3>
				<p>Swarnajayanti Gram Swarozgar Yojana launched by the ministry of rural development on April 1, 1999    for Rural Employment to Bring the assisted poor families above the poverty line by organizing them into Self Help Groups (SHGs) through the process of social mobilization, their training and capacity building and provision of income generating assets through a mix of bank credit and government subsidy.</p>

			<h3 id="servicehead3">45- Swavalamban</h3>
				<p>Swavalamban  launched by the ministry of finance on September 26, 2010, to be replaced by Atal Pension Yojana Pension pension scheme to the workers in the unorganized sector. Any citizen who is not part of any statutory pension scheme of the Government and contributes between Rs. 1000 and Rs. 12000/- per annum, could join the scheme. The Central Government shall contribute Rs. 1000 per annum to such subscribers.</p>

			<h3 id="servicehead3">46- Udisha </h3>
				<p>Udisha launched by the ministry of women and child development for Child Care and Training Program for ICDS workers.</p>

