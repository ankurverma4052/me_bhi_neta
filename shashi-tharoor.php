			<h2 id="servicehead2">Shashi Tharoor- Controversial Yet An Enchanting Personality</h2>

			<p>Shashi Tharoor was born on 9 March 1956 in London is a politician and a former diplomat and is currently serving as Member of Parliament and Chairman of the Parliamentary Standing Committee on External Affairs and All India Professionals Congress. He is also a globally recognized writer and has authored 16 bestselling books centered on India. He is a globally recognized speaker on India's economics and politics, as well as on freedom of the press, human rights, Indian culture, and international affairs. </p> 


			<h2 id="servicehead2">Career At The United Nations </h2>

			<p>United Nations career for Shashi Tharoor career began in 1978 as a staff member of the UN High Commissioner for Refugees in Geneva and later in 2006, he announced his retirement after finishing second in the selection for U.N. Secretary-General to Ban Ki-moon. </p>


			<h2 id="servicehead2">Political Career</h2>

			<p>In February 2007, speculation about his post-UN future, the media reported that Tharoor might be introduced into Council of Ministers of Congress government. In the same month, he was reported to be a finalist for the position of dean of the USC Annenberg School for Communication in Los Angeles, but he withdrew his name and became chairman of Dubai-based Afras Ventures, as it established the Afras Academy for Business Communication (AABC) in Thiruvananthapuram, Kerala. </p>

			<p>In March 2009 Tharoor contested the Indian General Elections as a candidate for the Congress Party from Thiruvananthapuram. And Despite criticism of being an elite outsider, he won the elections by a thumping margin of around 100,000.</p>

			<h2 id="servicehead2">Popularity As An Influential Speaker</h2>

			<p>Tharoor is especially notable for his extraordinary eloquence while speaking, as demonstrated by the immense popularity of, his speech decrying British Colonialism, delivered at the Oxford Union in 2015 has amassed over 3.9 million views on one site alone.</p>


			<h2 id="servicehead2">Controversies</h2>

			<p>In September 2009, Tharoor and S. M. Krishna were accused of staying in luxurious 5-star hotels on government expenses. Later, he said that he had paid out of his own pocket for the accommodation.</p>

			<p>Tharoor criticised Nehru for his conduct of Indian foreign policy and was distorted by the Indian media in January 2010. </p>

			<p>Another controversy involved Gandhi Jayanti when he said people should be working rather than staying at home if want to pay real homage to Mahatma Gandhi and his belief of work is worship.</p>

			<p>A controversy erupted and he was alleged to equate the traveling public to cattle. In his defense, Tharoor explained that "cattle class" was a well-established phrase for economy class travel, and that it attacked the airlines and not the passengers.</p>

			<p>In 2014, Tharoor was dropped as the official spokesperson of the party for expressing support to Swachh Bharat Abhiyan and being a pro-Modi.</p>

			<p>On 15 January 2014, a series of intimate messages, sent by the Pakistani journalist Mehr Tarar to Tharoor, was posted on his Twitter account which proclaimed Tarar's love for Tharoor. </p>

			<p>On 17 January 2014, a day after the Twitter controversy, Sunanda was found dead in room number 345 of the Leela Palace hotel in Chanakyapuri, New Delhi and Subramanian Swamy, a politician of the BJP, has tried to fight this case against Tharoor in January 2015 accusing him of involvement in her death. </p>

